t1 = (1,2,3); t1            ##t1이라는 튜플(1,2,3)을 생성한다
t2 = (4,5,6); t2            ##t2이라는 튜플(4,5,6)을 생성한다
t3 = (7,8,9); t3            ##t3이라는 튜플(7,8,9)을 생성한다
tt = (t1, t2, t3); tt       ##t1,t2,t3을 원소로 하는 이차원 튜플 tt를 생성한다

print(t1[0], t1[1], t1[2])  ##튜플의 원소들을 출력한다
print(t2[0], t2[1], t2[2])
print(t3[0], t3[1], t3[2])
