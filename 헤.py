a = int(input("숫자1 입력: "))   ##사용자가 값을 입력하면 그 값을 정수형으로 형변환하고 그 값을 변수a에 저장한다.##
b = int(input("숫자2 입력: "))   ##사용자가 값을 입력하면 그 값을 정수형으로 형변환하고 그 값을 변수b에 저장한다.##
print(a, '+', b, '=', a+b)       ##변수a와 변수b의 합을 구해 "a+b=a와 b의 합"의 형식으로 출력되도록한다.##
print(a, '*', b, '=', a*b)       ##변수a와 변수b의 곱을 구해 "a*b=a와 b의 곱"의 형식으로 출력되도록한다.##
print(a, '^', b, '=', pow(a,b))  ##a의 b제곱을 구해 "a^b=a의 b제곱"의 형식으로 출력되도록한다.##
