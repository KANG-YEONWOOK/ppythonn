a = 200                                      ##변수a에 값 200을 저장

if a < 100 :                                 ##a가 100보다 작다면
    print("100보다 작군요.")                 ##두 문장을 출력하고
    print("참이면 이 문장도 보이겠죠?") 
else :                                       ##a가 100보다 작지 않다면
    print("100보다 크군요.")                 ##아래 두문장을 출력한다.
    print("거짓이면 이 문장도 보이겠죠?")

print("프로그램 끝")                         ##a값에 상관없이 "프로그램 끝"을 출력한다.
